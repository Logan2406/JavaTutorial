package OOPs.Inheritence;

public class Parent {
    private String a;
    String b;
    public String c;

    public void setA(String a)
    {
        this.a = a;
    }

    public void setB(String b)
    {
        this.b = b;
    }

    public void setC(String c)
    {
        this.c = c;
    }
    public String getA()
    {
        return this.a;
    }

    public String getB()
    {
        return this.b;
    }

    public String getC()
    {
        return this.c;
    }
}
